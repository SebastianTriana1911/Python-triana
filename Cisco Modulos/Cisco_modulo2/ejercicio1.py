# El ejercicio numero 1 constara de hacer que el ususario digite un numero y este sea sumado, restado, 
# multiplicado y dividido por el mismo numero, haciendo que los resultados se muestren en consola 

numero1 = float(input("Por favor inserte un numero: "))

resultado = numero1 + numero1 # El simbolo + significa suma 

print ("El resultado de la suma es: ", resultado) 

resultado = numero1 - numero1 # El simbolo - significa resta 
print ("El resultado de la resta es: ", resultado)

resultado = numero1 * numero1 # El simbolo * significa multiplicacion
print ("El resultado de la multiplicacion es: ", resultado)

resultado = numero1 / numero1 # El simbolo / significa division
print ("El resultado de la division es: ", resultado)