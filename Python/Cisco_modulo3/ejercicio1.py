# El ejercicio 1 constara de una operacion simple de dos numeros insertados por un usuario, en estas operaciones se
# mostrara cual es la utilidad de los siguientes signos y cual seria el resultado, cabe aclarar que con algunos
# signos se podra ver un resultado booleano 

numero1 = int(input("Escriba el primero numero entero: "))
numero2 = int(input("Escriba el segundo numero entero: "))

print ("Los numeros que inserto fueron: ",numero1 ,"y", numero2)

print (numero1 == numero2) # El signo == (igual a) da a conocer  si una varable es igual que la otra, si si es igual mostrara True de lo contrario False

print (numero1 != numero2) # El signo != (no es igual a) da a conocer si una varable no es igual que la otra, si si es igual mostrara False de lo contrario True

print (numero1 >= numero2) # El signo >= (mayor o igual que) da a conocer si una varable es mayor o igual que la otra, si si es mayor o igual mostrara True de lo contrario False

print (numero1 <= numero2) # El signo <= (menor o igual que) da a conocer si una varable es menor o igual que la otra, si si es menor o igual mostrara True de lo contrario False

